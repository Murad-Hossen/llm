class GIN_MLP(nn.Module):
    def __init__(self, in_dim, hidden):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(in_dim, hidden), nn.ReLU(), nn.Linear(hidden, hidden)
        )

    def forward(self, x):
        return self.net(x)


class CGCC_GIN(nn.Module):
    def __init__(self, in_dim=5, n_stats=10, hidden=64, num_classes=3, drop=0.4):
        super().__init__()
        self.conv1 = GINConv(GIN_MLP(in_dim, hidden))
        self.conv2 = GINConv(GIN_MLP(hidden, hidden))
        self.bn1 = nn.BatchNorm1d(hidden); self.bn2 = nn.BatchNorm1d(hidden)
        self.head = nn.Sequential(
            nn.Linear(hidden * 2 + n_stats, hidden), nn.ReLU(), nn.Dropout(drop),
            nn.Linear(hidden, num_classes),
        )

    def forward(self, x, edge_index, batch, stats):
        h = F.relu(self.bn1(self.conv1(x, edge_index)))
        h = F.dropout(h, p=0.3, training=self.training)
        h = F.relu(self.bn2(self.conv2(h, edge_index)))
        gmean = global_mean_pool(h, batch)
        gmax = global_max_pool(h, batch)
        z = torch.cat([gmean, gmax, stats], dim=-1)
        return self.head(z)


