def orientation_entropy(G, n_bins=36):
    """Boeing 2019 grid-vs-organic indicator: entropy of compass bearings."""
    bearings = []
    for u, v, _ in G.edges(keys=True) if G.is_multigraph() else G.edges(data=True):
        if not G.is_multigraph():
            v = u[1]; u = u[0]
        try:
            x1, y1 = G.nodes[u]["x"], G.nodes[u]["y"]
            x2, y2 = G.nodes[v]["x"], G.nodes[v]["y"]
        except KeyError:
            continue
        dy, dx = y2 - y1, x2 - x1
        if dx == 0 and dy == 0:
            continue
        ang = math.degrees(math.atan2(dy, dx)) % 180.0  # undirected: fold to [0,180)
        bearings.append(ang)
    if not bearings:
        return 0.0
    h, _ = np.histogram(bearings, bins=n_bins, range=(0.0, 180.0))
    p = h / h.sum() + 1e-12
    H = -(p * np.log(p)).sum()
    return float(H / math.log(n_bins))  # normalized to [0, 1]


def graph_stats(G):
    n = G.number_of_nodes(); m = G.number_of_edges()
    if n == 0:
        return np.zeros(10, dtype=np.float32)
    # collapse multi-edges for clustering / transitivity
    Gs = nx.Graph(G)
    deg = np.array([d for _, d in G.degree()], dtype=np.float32)
    clu = nx.clustering(Gs)
    clu_arr = np.array([clu[n_] for n_ in Gs.nodes()], dtype=np.float32)
    try:
        trans = float(nx.transitivity(Gs))
    except ZeroDivisionError:
        trans = 0.0
    lengths = []
    for _, _, d in (G.edges(data=True) if not G.is_multigraph() else G.edges(keys=False, data=True)):
        if "length" in d:
            lengths.append(float(d["length"]))
    lengths = np.array(lengths, dtype=np.float32) if lengths else np.array([0.0], dtype=np.float32)
    density = (2.0 * m) / max(n * (n - 1), 1)
    return np.array([
        n, m, density,
        deg.mean(), deg.var(),
        clu_arr.mean() if len(clu_arr) else 0.0,
        trans,
        lengths.mean(), (lengths.std() / (lengths.mean() + 1e-6)),
        orientation_entropy(G),
    ], dtype=np.float32)


def build_pyg(G, label=None):
    nodes = list(G.nodes())
    if not nodes:
        return None
    idx = {n: i for i, n in enumerate(nodes)}
    n = len(nodes)

    xs = np.array([G.nodes[n_].get("x", 0.0) for n_ in nodes], dtype=np.float32)
    ys = np.array([G.nodes[n_].get("y", 0.0) for n_ in nodes], dtype=np.float32)
    xs = xs - xs.mean(); ys = ys - ys.mean()
    s = float(np.sqrt(xs.var() + ys.var()) + 1e-6)
    xs /= s; ys /= s

    sc = np.array([float(G.nodes[n_].get("street_count", 0)) for n_ in nodes], dtype=np.float32)
    sc = (sc - sc.mean()) / (sc.std() + 1e-6)

    deg_arr = np.array([G.degree(n_) for n_ in nodes], dtype=np.float32)
    deg_arr = (deg_arr - deg_arr.mean()) / (deg_arr.std() + 1e-6)

    Gs = nx.Graph(G)
    clu = nx.clustering(Gs)
    clu_arr = np.array([clu[n_] for n_ in nodes], dtype=np.float32)

    X = np.stack([xs, ys, sc, deg_arr, clu_arr], axis=1)

    # edges (undirected → both directions)
    e_pairs = []
    for u, v in (G.edges() if not G.is_multigraph() else set((u, v) for u, v in G.edges())):
        if u in idx and v in idx:
            iu, iv = idx[u], idx[v]
            e_pairs.append((iu, iv)); e_pairs.append((iv, iu))
    if not e_pairs:
        e_pairs = [(0, 0)]
    ei = torch.tensor(e_pairs, dtype=torch.long).t()

    stats = graph_stats(G)
    data = Data(x=torch.from_numpy(X), edge_index=ei,
                stats=torch.from_numpy(stats).unsqueeze(0))
    if label is not None:
        data.y = torch.tensor([label], dtype=torch.long)
    return data


